  // Write data to local screenbuffer
  SSD1306_GotoXY(0, 0);
  SSD1306_Puts("HELLO", &Font_11x18, 1);
  SSD1306_GotoXY(10, 30);
  SSD1306_Puts(" WORLD", &Font_11x18, 1);

  SSD1306_UpdateScreen();

  // Draw rectangle on screen
  for (uint8_t i=0; i<28; i++) {
      for (uint8_t j=0; j<64; j++) {
          SSD1306_DrawPixel(100+i, 0+j, 1);
      }
  }
  // Copy all data from local screenbuffer to the screen
  SSD1306_UpdateScreen();

  HAL_Delay(1000);
  SSD1306_Clear();

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  for (uint8_t i = 0; i < 7; i++){
	  HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
  	  HAL_Delay(200);
  }

  HAL_ADC_Start_IT(&hadc1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  TIM2->CCR1 = (adc_result<<4);
	  HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);

	  SSD1306_Fill (0);
	  SSD1306_GotoXY(0, 0);
	  sprintf(adc_result_str, "%u", adc_result);
	  sprintf(volts_str, "%.3f", compute_volts(adc_result));
	  SSD1306_Puts(adc_result_str, &Font_11x18, 1);
	  SSD1306_GotoXY(0, 18);
	  SSD1306_Puts(volts_str, &Font_11x18, 1);
	  SSD1306_GotoXY(55, 18);
	  SSD1306_Puts(" Volts", &Font_11x18, 1);
	  SSD1306_UpdateScreen();
	  // HAL_Delay(5);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
